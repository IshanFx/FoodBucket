public class User {
    private int userId;
    private String cusUName;
    private String cusPass;
    private String cusMail;
    
    
    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getCusUName() {
        return cusUName;
    }

    public void setCusUName(String cusUName) {
        this.cusUName = cusUName;
    }

    public String getCusPass() {
        return cusPass;
    }

    public void setCusPass(String cusPass) {
        this.cusPass = cusPass;
    }

    public String getCusMail() {
        return cusMail;
    }

    public void setCusMail(String cusMail) {
        this.cusMail = cusMail;
    }
    
    
    
}
